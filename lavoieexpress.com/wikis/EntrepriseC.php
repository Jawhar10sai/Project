<h4  style="margin-bottom:10px;">Environnement</h4>
<p class="description">
  Depuis sa création, La Voie EXPRESS a toujours eu comme objectif premier la satisfaction de ses clients. Grâce à sa volonté, sa réactivité et son management, elle a su répondre aux exigences des clients qui la consultent. Face à cette situation et afin de maintenir cet état d’esprit et sa position sur le marché, elle s’est inscrite dans une démarche qualité qui s’est couronnée par l’obtention d’un certificat <b>AFNOR ISO 9001</b> version 2008 en décembre 2012.

Ce certificat n’est pas une fin en soi, le système instauré en interne doit être efficace et répondre au mieux aux exigences des clients. Son maintien est conditionné par un fonctionnement basé sur l’amélioration continue qui permet à l’entreprise de garder son image de marque.

Le service qualité de La Voie EXPRESS au moment de l’audit initial de certification se composait d’un responsable, son assistante, et deux coordinateurs qualité.

Afin qu’elle puisse améliorer son image de marque, et avec l’accord et l’engagement de la direction générale, un call center et service après-vente ont été mis en place. Ces derniers étant rattachés au Département qualité pour un meilleur suivi et traitement des demandes clients.
</p>
<h4  style="margin-bottom:10px;">Social</h4>
<p class="description">
Notre objectif est que le développement durable devienne une réalité de tous les jours. Pour cela notre stratégie et notre démarche s'inscrit dans la "Green Attitude", avec des investissements et des Process respectant l'Environnement et les citoyens! De nombreux exemples: véhicules avec moteur électronique basse consommation, filtre catalytique, réduction du bruit inférieur à 40 DB, recyclage et récupération des eaux des Entrepôts Logistique, appareillage basse consommation, récupération et dématérialisation de documents, etc...
</p>
