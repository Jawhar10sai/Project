<h4 class="sub-titlessection">Afrrètement</h4>
<p class="description">
    La Voie EXPRESS opère depuis de nombreuses années en affrètement sur le marché national. Cette solution vous garantit des services compétitifs et de qualité tout en vous permettant de faire face à de fortes variations de demande.
</p>
<p class="description">
  Nous nous appuyons sur un parc roulant comprenant différents types de véhicules :<br>
    5t5, 8t, 14t, semi remorques 25t : tot-liner, bâchées, surbaissée, col de signe…
</p>
<p class="description">
  Et ce dans un souci de repondre exactement à la demande et au besoin du client.
</p>
<p class="description">
