    <li class="description">La prise de rendez-vous au chargement ou chez le destinataire</li>
    <li class="description">La gestion des non-conformités</li>
    <li class="description">La possibilité d'échanges de données : notre service informatique s'adapte à vos systèmes d'information</li>
  </ul>
</p>
<p class="description">
  Nous vous assurons ainsi une grande réactivité et flexibilité, associé à un niveau de qualité et de sécurité optimal.
</p>
